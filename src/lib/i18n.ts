export type CopyKey = keyof typeof copy.en;

const LOCALE_KEY = "revive-motion-locale";

export function getStoredLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const stored = localStorage.getItem(LOCALE_KEY);
  return stored === "es" ? "es" : "en";
}

export function setStoredLocale(locale: Locale) {
  if (typeof window !== "undefined") {
    localStorage.setItem(LOCALE_KEY, locale);
  }
}

export function resolveLocale(user?: { language?: Locale } | null): Locale {
  return user?.language ?? getStoredLocale();
}

export function t(key: CopyKey, locale: Locale = "en"): string {
  return copy[locale][key] ?? copy.en[key];
}
